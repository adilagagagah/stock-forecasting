# 2. Satukan semua data menjadi 1 kolom panjang (Pooling Data)
semua_persentase = np.array(data_matriks).flatten() * 100  # Ubah ke skala persen 0-100%

# 3. Hitung statistik nilai batas penting
q1 = np.percentile(semua_persentase, 25)
median = np.percentile(semua_persentase, 50)
q3 = np.percentile(semua_persentase, 75)

# 4. Gambar Boxplot
plt.figure(figsize=(6, 8))
sns.set_theme(style="whitegrid")

# Gunakan orientasi vertikal 'v' agar mudah melihat sebaran bawah ke atas
ax = sns.boxplot(
    y=semua_persentase,
    color="skyblue",
    width=0.4,
    linewidth=2,
    flierprops={"marker": "o", "markerfacecolor": "red", "markersize": 5},
)

# Tambahkan garis horizontal penanda nilai Q3 (Threshold Universal Anda)
plt.axhline(
    y=q3,
    color="red",
    linestyle="--",
    linewidth=1.5,
    label=f"Threshold (Q3 = {q3:.1f}%)",
)

# Pengaturan teks dan label
plt.title(
    "Boxplot Distribusi Persentase Parameter\n(Gabungan Seluruh Kota)",
    fontsize=12,
    fontweight="bold",
    pad=15,
)
plt.ylabel("Nilai Persentase (%)", fontsize=11)
plt.ylim(-5, 105)

# Tampilkan info nilai statistik langsung di grafik
plt.text(0.25, q1, f"Q1: {q1:.1f}%", va="center", ha="left", color="blue")
plt.text(0.25, median, f"Median: {median:.1f}%", va="center", ha="left", color="green")
plt.text(0.25, q3, f"Q3: {q3:.1f}%", va="bottom", ha="left", color="red", fontweight="bold")

plt.legend(loc="upper right")
plt.tight_layout()
plt.show()